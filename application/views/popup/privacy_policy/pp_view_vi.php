		<p>Cookies là những tập tin văn bản nhỏ mà trang web gửi tới máy tính của bạn hoặc thiết bị kết nối Internet khác để nhận diện trình duyệt hoặc để lưu trữ thông tin hoặc các cài đặt trong trình duyệt của bạn. Trình duyệt của bạn có thể cho bạn biết cách để được thông báo khi bạn nhận được một dạng cookies nào đó và cách để hạn chế hoặc tắt một vài cookies. Tuy nhiên, xin lưu ý rằng, nếu không có cookies bạn có thể sẽ không được sử dụng tất cả các tính năng trong trang web của chúng tôi.</p>
		<p>Để kết hợp thông tin chứa dựng qua cookies, máy chủ của chúng tôi có thể ghi nhận các chi tiết chẳng hạn như loại hệ thống điều hành, loại trình duyệt, tên miền và các cài đặt hệ thống khác, cũng như ngôn ngữ hệ thống bạn sử dụng và quốc gia và giờ khu vực mà thiết bị của bạn đang định vị. Máy chủ cũng có thể ghi lại thông tin chẳng hạn như địa chỉ trang web kết nối bạn với trang của chúng tôi và địa chỉ IP của thiết bị bạn sử dụng để kết nối Internet.</p>
		<p>Để kiểm soát máy chủ nào thu thập thông tin, chúng tôi có thể đánh dấu trên trang web của chúng tôi được gọi là “web beacons”. Đày là những hướng dẫn trên máy tính giúp kết nối trang web tới những máy chủ và cookies.</p>

